package edyoda.assignment2;

public class Sum {
    public static void main(String[] args) {
        int a=5;
        int b=7;
        int c=9;
        int sum1=a+b;
        int sum2=a+b+c;
        int sum3=a*b;
        int sum4=a*b*c;
        System.out.println("Add two numbers:"+sum1);
        System.out.println("Add three numbers:"+sum2);
        System.out.println("Multiple two numbers:"+sum3);
        System.out.println("Multiple three numbers:"+sum4);
    }
}